O QUE SÃO OBEJTOS?

Objetos são entidadades que possuem comportamentos e características.

Características são conhecidas como propriedades (variáveis).

Os comportamentos como métodos (funções).

Os objetos interagem entre si e sistemas são escrito em POO.

É possível no PHP desenvolver neste paradigma ( programação orientada a objetos)

----------------------------------------------------------------------------------

O QUE SÃO CLASSES?

Classe são os "pais" dos objetos.

Em PHP sempre que vamos criar ou instanciar um objeto vamos precisar de uma classe.

A classe contém o molde do objeto, ou seja, seus métodos e suas propriedades.

Podemos mudar o valor para cada objeto criado, mas ele parte do que a clase impõe.

