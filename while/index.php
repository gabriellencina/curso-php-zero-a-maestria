<?php

// O while é uma estrutura de repetição, pode executar o código n vezes até satisfazer sua condição

// Geralmente é necessário um contador para atingir a condição


// Definiçaõ do contador
$x = 0;

// inicio do loop / estrutura
while ($x < 10 ) {

    // Corpo do loop
    echo $x . "<br>";

    // Incremento do contador
    $x++;

}

echo "Continuando o código <br>";



